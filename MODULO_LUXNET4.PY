def carregar_eeg(caminho_arquivo):
    logger.info(f"Carregando EEG: {caminho_arquivo}")
    raw = mne.io.read_raw_fif(caminho_arquivo, preload=True)
    raw.pick_types(eeg=True)
    raw.filter(1., 40., fir_design='firwin')  # Filtra entre 1-40 Hz
    return raw

def extrair_bandpower(raw, bandas=None):
    if bandas is None:
        bandas = {
            'delta': (0.5, 4),
            'theta': (4, 8),
            'alpha': (8, 13),
            'beta': (13, 30)
        }
    psd, freqs = mne.time_frequency.psd_welch(raw, fmin=0.5, fmax=40.0, n_fft=2048)
    bandpower = {}
    for banda, (fmin, fmax) in bandas.items():
        mask = (freqs >= fmin) & (freqs <= fmax)
        bandpower[banda] = np.mean(psd[:, mask], axis=1)
        logger.info(f"Pot√™ncia {banda}: m√©dia geral = {np.mean(bandpower[banda]):.4e}")
    return bandpower

def gerar_params_luxnet(bandpower):
    n = len(next(iter(bandpower.values())))
    params = {}
    for coef, _, num, den, _, _ in HIERARQUIA:
        params[coef] = np.random.uniform(0.1, 1.0, n)
        params[num] = bandpower.get(num, np.random.uniform(1e-15, 1e-12, n))
        params[den] = bandpower.get(den, np.random.uniform(1e-15, 1e-12, n))
        # Ajuste para evitar zero denominador
        params[den] = np.where(params[den] == 0, 1e-15, params[den])
    return params

