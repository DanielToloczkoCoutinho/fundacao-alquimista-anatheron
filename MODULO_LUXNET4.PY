import numpy as np
import mne
import matplotlib.pyplot as plt
from IPython.display import display

# Gera√ß√£o de EEG sint√©tico para medita√ß√£o
sfreq = 256
t = np.arange(0, 10, 1/sfreq)
eeg = 0.7 * np.sin(2 * np.pi * 10 * t + np.random.uniform(0, 2*np.pi))  # Alfa dominante
eeg += 0.2 * np.sin(2 * np.pi * 20 * t + np.random.uniform(0, 2*np.pi))  # Beta reduzido
eeg += np.random.normal(0, 0.05, len(t))  # Ru√≠do baixo, t√≠pico de medita√ß√£o

ch_names = ['simulated_meditation']
info = mne.create_info(ch_names=ch_names, sfreq=sfreq, ch_types='eeg')
raw = mne.io.RawArray(eeg.reshape(1, -1), info)
raw.filter(1, 40, method='fir', fir_design='firwin')

# Par√¢metros LuxNet
PARAMS = {'c_alpha': 0.7, 'omega_alpha': 2 * np.pi * 10, 'c_beta': 0.5, 'omega_beta': 2 * np.pi * 20}
bands = {'alpha': (8, 13), 'beta': (13, 30)}

def compute_E(bp, t=0):
    E = 0
    for band in ['alpha', 'beta']:
        coef = PARAMS[f'c_{band}']
        omega = PARAMS[f'omega_{band}']
        vib = np.sin(omega * t)
        E += coef * np.mean(bp[band]) * vib
    return E

# Processamento
psd, freqs = mne.time_frequency.psd_welch(raw, fmin=0.5, fmax=40, n_fft=512)
bandpower = {b: np.mean(psd[:, (freqs >= f0) & (freqs <= f1)]) for b, (f0, f1) in bands.items()}
times = np.linspace(0, 10, len(t))
Es = [compute_E(bandpower, ti) for ti in times]

# Visualiza√ß√£o
plt.figure(figsize=(10, 5))
plt.plot(times, Es, color='crimson', lw=2)
plt.title('E_total (LuxNet) com EEG Sint√©tico - Medita√ß√£o')
plt.xlabel('Tempo (s)')
plt.ylabel('E_total (unidades arbitr√°rias)')
plt.grid(True)
plt.show()

print(f"E_total m√©dio: {np.mean(Es):.4f}, Desvio: {np.std(Es):.4f}")
